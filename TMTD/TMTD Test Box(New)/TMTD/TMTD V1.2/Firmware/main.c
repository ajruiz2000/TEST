void main(void)

{   
	int AD_Value_Old = 0;
	int AD_Value;
	int *AD_ValuePointer;
	float frequency = 0.0;
	int x1 = 0;
	int MovieGateStateOld; 
//	char Version[64] = "QFI TMTD Firmware Version 1.00                  ";
	TimeDelayPointer = &TimeDelay;
	AD_ValuePointer = &AD_Value;	
	TimeDelay = 60848;


	
    InitializeSystem();
	//Check Version (if 16x1 or 16x2 display
	// 1 = 16x2 display
	// 0 = 16x1 diaplay
	DisplayVersion = CheckVersion();
	//Taken out it is too slow
	//LCDscroll_str(Version);  
	initTMTDCommands();


	T0CONbits.TMR0ON = 1; //Timer On
    while(1)
    {
        
	//Must call periodically
     USBDeviceTasks();  

	
	 if (MG_Switch == 0) //MG Mode
		{
		//Check to see if just switched to MG Mode
		if (MG_Mode == 0)
			{
		    MovieGateStateOld = SetMGMode(TimeDelayPointer);
			}
		//Check to see if MG state has changed
		MovieGateStateOld = CheckMGState(MovieGateStateOld);
		}




	else  //We are in either DC or Pulse Mode
		{
		if (DC_Switch == 0)  //In DC Mode Measure Current
			{
			if (DC_Mode == 0) // Just came from pulse mode set stuff
				{
				SetDCMode(TimeDelayPointer);
				}
			}
		else        // In Pulse Mode...set frequency
			{
			if (Pulse_Mode == 0 ) //Just came from DC or MG Mode set stuff
				{
				SetPulseMode();
				AD_Value = SampleFrequencyAD();
				frequency = SetFrequency(TimeDelayPointer, AD_ValuePointer);
				AD_Value_Old = AD_Value;
				x1 = 0;
				}

			if (x1 == 10) // check a/d every to cycles
				{
				AD_Value = SampleFrequencyAD();
				x1 = 0;
				if (((AD_Value_Old - AD_Value) <= 10) && ((AD_Value_Old - AD_Value) >= -10) )
					{
					//If no change in AD go on with program
					}
				else
					{
					//iF AD change set new frequency
					frequency = SetFrequency(TimeDelayPointer, AD_ValuePointer);
					AD_Value_Old = AD_Value;
			
					} 
				}
			else
				{
				x1++;
				}
			

			}
		}
		// Application-specific tasks.
		// Application related code may be added here, or in the ProcessIO() function.
        ProcessIO();        
    }//end while
}//end main


